function X = thomas(P,Q,R,W,M)
    X = zeros(6,1,M); H = X;
    H(:,:,1) = Q(:,:,1)\W(:,:,1);
    G(:,:,1) = Q(:,:,1)\R(:,:,1);
    for i=2:M-1
        H(:,:,i) = (Q(:,:,i) - P(:,:,i)*G(:,:,i-1)) \ (W(:,:,i) - P(:,:,i)*H(:,:,i-1));
        G(:,:,i) = (Q(:,:,i) - P(:,:,i)*G(:,:,i-1)) \ R(:,:,i);
    end

    X(:,:,M-1) = H(:,:,M-1);
    for i=M-2:-1:1
        X(:,:,i) = H(:,:,i) - G(:,:,i)*X(:,:,i+1);
    end
end